function applyPairs(text) {
  let out = text;
  for (const [from, to] of PAIRS) {
    if (out.includes(from)) out = out.split(from).join(to);
  }
  return out;
}

function patchUiBlock(src) {
  let out = src;
  for (const [key, val] of Object.entries(UI_EXACT)) {
    const re = new RegExp(`(${key}:\\s*)(['\`])((?:\\\\.|(?!\\2).)*)\\2`, 's');
    out = out.replace(re, `$1$2${val.replace(/\\/g, '\\\\').replace(/\$/g, '$$$$')}$2`);
  }
  return out;
}

function patchQuotes(src) {
  let out = src;
  for (const [from, to] of QUOTE_EXACT) {
    out = out.split(from).join(to);
  }
  return out;
}

function localizeFile(filePath) {
  const rel = path.relative(root, filePath);
  let text = fs.readFileSync(filePath, 'utf8');
  const original = text;
  if (rel.endsWith('i18n.zh-hant.js')) {
    text = patchUiBlock(text);
    text = patchQuotes(text);
  }
  text = applyPairs(text);
  if (text !== original) {
    fs.writeFileSync(filePath, text, 'utf8');
    console.log('updated', rel);
  } else {
    console.log('unchanged', rel);
  }
}

